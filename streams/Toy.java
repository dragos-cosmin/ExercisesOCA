package org.example.streams;

public class Toy {
    private String shade;
    private int size;

    public Toy(String shade, int size) {
        this.shade = shade;
        this.size = size;
    }

    public String getShade() {
        return shade;
    }

    public void setShade(String shade) {
        this.shade = shade;
    }

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }
}
